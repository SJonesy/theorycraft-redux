package Races;

public class Elf extends Race {
    public Elf() {
        this.setName("Elf");
        this.setMightModifier(0);
        this.setIntellectModifier(1);
        this.setConstitutionModifier(0);
        this.setDexterityModifier(3);
        this.setPerceptionModifier(3);
        this.setResolveModifier(0);
    }
}

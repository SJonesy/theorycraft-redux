package Races;

public class Elf extends Race {
    public Elf() {
        this.setName("Elf");
        this.setMightModifier(0);
        this.setIntellectModifier(1);
        this.setConstitutionModifier(-1);
        this.setDexterityModifier(2);
        this.setPerceptionModifier(2);
        this.setResolveModifier(0);
    }
}
